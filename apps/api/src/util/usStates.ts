const CODE_SET = new Set(US_STATES.map((s) => s.code));
const NAME_TO_CODE = new Map(
  US_STATES.map((s) => [s.name.toLowerCase().replace(/\./g, "").replace(/\s+/g, " ").trim(), s.code] as const)
);

export function normalizeUsStateToCode(input: unknown): string | null {
  if (typeof input !== "string") return null;
  const raw = input.trim();
  if (!raw) return null;

  const upper = raw.toUpperCase();
  if (upper.length === 2 && CODE_SET.has(upper)) return upper;

  const normalizedName = raw.toLowerCase().replace(/\./g, "").replace(/\s+/g, " ").trim();
  const code = NAME_TO_CODE.get(normalizedName);
  return code ?? null;
}


